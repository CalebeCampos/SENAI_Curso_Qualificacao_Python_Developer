
# .title() ajusta a string para o modo titulo com as inicias de cada palavra maisculas
nome = input("Informe seu nome: ").title()
print(f"Seu nome Ã©: {nome}")

# .capitalize() ajusta a string para o modo frase com a inicial da frase em maiscula
texto = input("Informe uma frase: ").capitalize()
print(f"Frase: {texto}")

# .upper() ajusta a string para caixa alta todas as letras
texto = input("Informe uma frase em caixa baixa: ").upper()
print(f"Frase: {texto}")

# .lower() ajusta a string para caixa baixa todas as letras
texto = input("Informe uma frase em caixa alta: ").lower()
print(f"Frase: {texto}")